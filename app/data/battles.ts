export function getTargetsForBattle(battleNumber: number) {
  return battles.find((battle) => battle.number === battleNumber)?.targets || []
}

export function getTargetInfo(targetNumber: number) {
  for (const battle of battles) {
    const target = battle.targets.find(
      (target) => target.number === targetNumber
    )

    if (target) {
      return {
        ...target,
        battleNumber: battle.number,
        battleName: battle.name
      }
    }
  }

  throw new Error('Target not found')
}
